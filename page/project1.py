import streamlit as st
import os
from src.utils import project1_desc as p1d


def app():
    st.write("""
    ### Streamlit 매뉴얼을 작성해 주세요.
    실행 방법: 
             
             1. 가상환경 실행: conda activate new_env

             2. 리포지터리 경로 이동

             3. streamlit 실행 명령어 streamlit run app.py

             ## 🚀 빠른 시작

            ### 1단계: 앱 접속
            ```bash
            streamlit run app.py
            ```

            ### 2단계: 질문 입력
            - 하단 채팅창에 질문 입력
            - Enter 또는 전송 버튼 클릭

            ### 3단계: 답변 확인
            - 2-3초 후 AI 답변 생성
             

            - 추가 질문 가능
             
    ## 💡 질문 유형

    ### 📊 통계 질문 (개수/통계)

    **키워드:** 몇 개, 개수, 통계, 비율

    **예시:**
    ```
    서울 한식 몇 개?
    경기도 음식점 개수
    제주도 가맹점 통계
    서울 가맹점 업종 통계
    ```         
    **제공 정보:**
    - 정확한 개수
    - 종류별 분포
    - 대표 가맹점 10개
    - 디지털 상품권 여부

    ---

    ### 🔍 추천/검색 질문

    **키워드:** 추천, 알려줘, 찾아줘, 어디

    **예시:**
    ```
    서울 유명한 카페 추천
    강남역 근처 맛집
    디지털 상품권 되는 곳
    부산 자전거 가게 알려줘
    ```

    **제공 정보:**
    - 3-5개 추천
    - 가맹점 특징
    - 위치 정보
    - 디지털 상품권 여부
         
    ---

    ### 💬 대화형 질문

    **연속 질문 가능:**
    ```
    사용자: "서울 음식점 알려줘"
    챗봇: [15,000개 결과 요약]

    사용자: "중국음식만"
    챗봇: [500개로 필터링]

    사용자: "디지털 되는 곳"
    챗봇: [300개로 재필터링]
    ```

    ---
             
    ## 🎯 질문 잘하는 법

    ### ✅ Good Examples

    | 질문 | 이유 |
    |------|------|
    | "서울 자전거 몇 개?" | 지역 + 업종 + 의도 명확 |
    | "강남역 근처 맛집 추천" | 위치 + 카테고리 + 의도 |
    | "디지털 되는 카페" | 조건 + 카테고리 |

    ### ❌ Bad Examples

    | 질문 | 문제점 | 개선 |
    |------|--------|------|
    | "카페" | 너무 광범위 | "서울 카페 추천" |
    | "많이 있어?" | 의도 불명확 | "서울 가맹점 몇 개?" |
    | "거기" | 맥락 없음 | "강남역 근처" |         

    """)

    st.subheader("📌 챗봇 실행 화면 예시")

    images = [
		("docs/서울 가맹점 수.png", "서울 가맹점 수 조회"),
		("docs/경기 자전거 가맹점 수.png", "경기 자전거 가맹점 수"),
		("docs/서울 가맹점 통계.png", "서울 가맹점 통계"),
		("docs/서울 업종 비율.png", "서울 업종 비율"),
		("docs/RAG 기반 답변.png", "RAG 기반 답변")
	]

    for path, caption in images:
        # 파일이 실제로 존재하는지 체크
        if os.path.exists(path):
            st.image(path, caption=caption, use_container_width=True)
        else:
        	# 파일이 없으면 경고 메시지 출력 (디버깅용)
            st.error(f"❌ 파일을 찾을 수 없습니다: {path}")
            # 현재 작업 디렉토리를 출력해서 경로를 맞게 설정했는지 확인
            st.write(f"현재 경로: {os.getcwd()}")

    p1d.desc()
